    public void AtualizaTabela(){
        try {
            conectar.conectaBanco();
            this.conectar.executarSQL(
                "SELECT * From Pedido where Pedido_Status = 'Preparo';"
            );
            ResultSet rs = this.conectar.getResultSet();
            ArrayList<Pedido> p1 = new ArrayList<>();
            while (rs.next()) {
                p1.add(new Pedido(rs.getInt("Pedido_ID") ,
                        rs.getString("Pedido_Nome") ,
                        rs.getString("Pedido_Status")));
            }
            
            ArrayList<String> colunas = new ArrayList<String>();
            colunas.add("ID");
            colunas.add("Nome");
            colunas.add("Status");
            
            TPedido.setModel(utils.TableModelCreator.createTableModel(Pedido.class, p1, colunas));
            
        } catch (Exception e) {
            System.out.println("Erro ao consultar Banco " + e.getMessage());
        } finally {
            this.conectar.fechaBanco();
        }
    }
    
